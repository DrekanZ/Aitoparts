package com.example.aitoparts;

public class Book {
    int id;
    String nama;
    String mobil;
    String tanggal;
    String jam;
    String namaMontir;

    public Book(int id, String nama, String mobil, String tanggal, String jam, String namaMontir) {
        this.id = id;
        this.nama = nama;
        this.mobil = mobil;
        this.tanggal = tanggal;
        this.jam = jam;
        this.namaMontir = namaMontir;
    }
}
